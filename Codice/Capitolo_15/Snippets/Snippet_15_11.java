package LibroJava.Capitolo15;

/**
 * Mostra come utilizzare il tag {@code {@literal}}.
 *
 * @author Pellegrino Principe
 */
public class Snippet_15_11
{
    /**
     * Questo metodo effettua una comparazione di tipo <i>Greater Than</i>.
     *
     * <p>Si comporta come l'operatore {@literal >}</p>
     */
    public boolean GT(int left, int right) { return left > right; }

    /**
     * Questo metodo effettua una comparazione di tipo <i>Lesser Than</i>
     *
     * <p>Si comporta come l'operatore {@literal <}</p>
     */
    public boolean LT(int left, int right) { return left < right; }
}
